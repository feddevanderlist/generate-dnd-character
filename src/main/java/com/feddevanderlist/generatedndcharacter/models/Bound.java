package com.feddevanderlist.generatedndcharacter.models;

public class Bound {
    double origin;
    double bound;

    public Bound(final double origin, final double bound) {
        this.origin = origin;
        this.bound = bound;
    }

    public double getOrigin() {
        return origin;
    }

    public double getBound() {
        return bound;
    }
}
